a=turtle.Turtle()
a.write("(0,0)")
a.pendown()
a.goto(-300,0)
a.write("(-300,0)")

a.goto(300,0)
a.write("(300,0)")

a.penup()
a.backward(300)
a.left(90)
a.pendown()
a.goto(0,-300)
a.write("(0,-300)")
a.goto(0,300)
a.write("(0,300)")


